package Satsuki::Base::Console;
our $VERSION = '1.00';
#------------------------------------------------------------------------------
###############################################################################
# ■基本処理
###############################################################################
#------------------------------------------------------------------------------
# ●【コンストラクタ】
#------------------------------------------------------------------------------
sub new {
	my $self = bless({}, shift);
	$self->{ROBJ} = shift;

	$ROBJ->{No_lastmodified} = 1;
	$ROBJ->{No_httpheader}   = 1;
	return $self;
}

#------------------------------------------------------------------------------
# ●【デストラクタ】
#------------------------------------------------------------------------------
sub DESTROY {
}

###############################################################################
# ■メインルーチン
###############################################################################
#------------------------------------------------------------------------------
# ●改行まで読み込み
#------------------------------------------------------------------------------
sub read_line {
	my $self = shift;
	my ($fh) = @_;
	if ($fh eq '') { $fh = 'STDIN'; }
	my $string = <$fh>;
	return $string;
}



1;
